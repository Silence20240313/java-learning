package com.itheima.define;

public class ArrayDemo3 {
    public static void main(String[] args) {
        // TODO 掌握数组的遍历
        int[] ages = {12,24,36};
        //            0   1  2

        //使用循环遍历   fori
        //快捷键 ages.length.fori 回车
        for (int i = 0; i < ages.length ; i++) {
            //  i = 0 1 2
            System.out.println(ages[i]);
        }
    }
}

